fn main() {
    // Declare variables of different types
    let integer_var: i32 = 42; // Integer type
    let float_var: f64 = 3.14; // Floating-point type
    let boolean_var: bool = true; // Boolean type
    let char_var: char = 'R'; // Character type

    // Print the values of each variable
    println!("Integer: {}", integer_var);
    println!("Floating-point: {}", float_var);
    println!("Boolean: {}", boolean_var);
    println!("Character: {}", char_var);
}

